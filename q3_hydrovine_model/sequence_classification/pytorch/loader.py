"""
Q3-HydroVine-model loader implementation for sequence classification.

Fine-tuned BERT classifier that categorizes business communications into
Client & Partner Communications, HR & Internal Admin, Operations & Maintenance,
Product & Engineering, and Sales & Marketing.
"""
from typing import Optional

from ....config import (
    ModelInfo,
    ModelGroup,
    ModelTask,
    ModelSource,
    Framework,
    StrEnum,
    LLMModelConfig,
)
from ....base import ForgeModel


class ModelVariant(StrEnum):
    """Available Q3-HydroVine-model variants for sequence classification."""

    Q3_HYDROVINE = "Q3-HydroVine-model"


class ModelLoader(ForgeModel):
    """Q3-HydroVine-model loader implementation for sequence classification."""

    _VARIANTS = {
        ModelVariant.Q3_HYDROVINE: LLMModelConfig(
            pretrained_model_name="TextAsData/Q3-HydroVine-model",
            max_length=128,
        ),
    }

    DEFAULT_VARIANT = ModelVariant.Q3_HYDROVINE

    def __init__(self, variant: Optional[ModelVariant] = None):
        super().__init__(variant)
        self.tokenizer = None

    @classmethod
    def _get_model_info(cls, variant: Optional[ModelVariant] = None) -> ModelInfo:
        if variant is None:
            variant = cls.DEFAULT_VARIANT
        return ModelInfo(
            model="Q3-HydroVine-model",
            variant=variant,
            group=ModelGroup.VULCAN,
            task=ModelTask.NLP_TEXT_CLS,
            source=ModelSource.HUGGING_FACE,
            framework=Framework.TORCH,
        )

    def load_model(self, *, dtype_override=None, **kwargs):
        from transformers import AutoModelForSequenceClassification, AutoTokenizer

        pretrained_model_name = self._variant_config.pretrained_model_name

        self.tokenizer = AutoTokenizer.from_pretrained(pretrained_model_name)

        model_kwargs = {}
        if dtype_override is not None:
            model_kwargs["torch_dtype"] = dtype_override
        model_kwargs |= kwargs

        model = AutoModelForSequenceClassification.from_pretrained(
            pretrained_model_name, **model_kwargs
        )
        model.eval()
        return model

    def load_inputs(self, dtype_override=None):
        if self.tokenizer is None:
            from transformers import AutoTokenizer

            self.tokenizer = AutoTokenizer.from_pretrained(
                self._variant_config.pretrained_model_name
            )

        sample_text = (
            "Hi team, please review the Q3 sales forecast and let me know "
            "if the projected numbers for our enterprise accounts look right."
        )

        inputs = self.tokenizer(
            sample_text,
            max_length=self._variant_config.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )

        return inputs

    def decode_output(self, co_out, framework_model=None):
        predicted_class_id = co_out[0].argmax().item()
        if (
            framework_model
            and hasattr(framework_model, "config")
            and hasattr(framework_model.config, "id2label")
        ):
            predicted_label = framework_model.config.id2label[predicted_class_id]
            print(f"Predicted label: {predicted_label}")
        else:
            print(f"Predicted class id: {predicted_class_id}")
