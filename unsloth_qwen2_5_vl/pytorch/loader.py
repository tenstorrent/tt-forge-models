"""
unsloth/Qwen2.5-VL-3B-Instruct model loader implementation for vision-language tasks.
"""
from transformers import AutoModelForImageTextToText, AutoProcessor
from typing import Optional


from ...base import ForgeModel
from ...config import (
    LLMModelConfig,
    ModelInfo,
    ModelGroup,
    ModelTask,
    ModelSource,
    Framework,
    StrEnum,
)


class ModelVariant(StrEnum):
    """Available unsloth/Qwen2.5-VL model variants for vision-language tasks."""

    QWEN_2_5_VL_3B_INSTRUCT = "3B_Instruct"


class ModelLoader(ForgeModel):
    """unsloth/Qwen2.5-VL-3B-Instruct model loader for vision-language tasks."""

    _VARIANTS = {
        ModelVariant.QWEN_2_5_VL_3B_INSTRUCT: LLMModelConfig(
            pretrained_model_name="unsloth/Qwen2.5-VL-3B-Instruct",
        ),
    }

    DEFAULT_VARIANT = ModelVariant.QWEN_2_5_VL_3B_INSTRUCT

    sample_image = (
        "https://qianwen-res.oss-cn-beijing.aliyuncs.com/Qwen-VL/assets/demo.jpeg"
    )

    def __init__(self, variant: Optional[ModelVariant] = None):
        super().__init__(variant)
        self.processor = None

    @classmethod
    def _get_model_info(cls, variant: Optional[ModelVariant] = None) -> ModelInfo:
        if variant is None:
            variant = cls.DEFAULT_VARIANT

        return ModelInfo(
            model="unsloth-Qwen2.5-VL-3B-Instruct",
            variant=variant,
            group=ModelGroup.VULCAN,
            task=ModelTask.MM_CONDITIONAL_GENERATION,
            source=ModelSource.HUGGING_FACE,
            framework=Framework.TORCH,
        )

    def load_model(self, *, dtype_override=None, **kwargs):
        pretrained_model_name = self._variant_config.pretrained_model_name

        model_kwargs = {}
        if dtype_override is not None:
            model_kwargs["torch_dtype"] = dtype_override
        model_kwargs |= kwargs

        self.processor = AutoProcessor.from_pretrained(pretrained_model_name)

        model = AutoModelForImageTextToText.from_pretrained(
            pretrained_model_name, **model_kwargs
        ).eval()

        return model

    def load_inputs(self, dtype_override=None, batch_size=1):
        messages = [
            {
                "role": "user",
                "content": [
                    {
                        "type": "image",
                        "image": self.sample_image,
                    },
                    {"type": "text", "text": "Describe this image."},
                ],
            }
        ]

        inputs = self.processor.apply_chat_template(
            messages,
            tokenize=True,
            add_generation_prompt=True,
            return_dict=True,
            return_tensors="pt",
        )
        return inputs
